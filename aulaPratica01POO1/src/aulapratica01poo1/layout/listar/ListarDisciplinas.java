    private void AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarActionPerformed
        // TODO add your handling code here:
        int index=TabelaListarDisciplina.getSelectedRow();
        if(index>-1)
        {
            int erros=0;
            Color colorError= new Color(255, 77, 77);  
            Disciplina d = DadosDisciplina.getDados().get(index);
            
            
            //variaveis
            int codigo=0;
            String nome=(String)this.TabelaListarDisciplina.getValueAt(TabelaListarDisciplina.getSelectedRow(), 0);
            int carga_horaria=0;
            
            try
            {
                codigo=Integer.parseInt((String)TabelaListarDisciplina.getValueAt(TabelaListarDisciplina.getSelectedRow(), 1));
            }catch(Exception e)
            {
                erros++;
                TabelaListarDisciplina.setBackground(colorError);
            }
            try
            {
                carga_horaria=Integer.parseInt((String)TabelaListarDisciplina.getValueAt(TabelaListarDisciplina.getSelectedRow(), 2));
            }catch(Exception e)
            {
                erros++;
                TabelaListarDisciplina.setBackground(colorError);
            }
            
            
            if(erros==0)
            {
                d.setCarga_horaria(carga_horaria);
                d.setCodigo(codigo);           
                d.setNome(nome);
                mostrarDadosTabela(TabelaListarDisciplina);                
                DadosDisciplina.salvar();
                JOptionPane.showMessageDialog(null, "Dados alterados com sucess.");
            }
            
            
            
            
            
            
            
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Selecionar uma linha.");
        }
    }//GEN-LAST:event_AlterarActionPerformed

