    public void mostrarDadosTabela(JTable tabela)
    {
      
         String[] colunas = new String[] {"Nome","Codigo","Carga Horaria"};
         String[][] dados = new String[0][0];
         
         DefaultTableModel modelo = new DefaultTableModel(dados,colunas);
         
         tabela.setModel(modelo);
         
         
         for(Disciplina d : DadosDisciplina.getDados())
         {
             
             String codigo;
             String cargahoraria;
             
             
            codigo = Integer.toString(d.getCodigo());
           cargahoraria = Integer.toString(d.getCarga_horaria());
             
             modelo.addRow(new String[] { d.getNome(),codigo,cargahoraria});
             
         }
         
        
       
       
    }
 
