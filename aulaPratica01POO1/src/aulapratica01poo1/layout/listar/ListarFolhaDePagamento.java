    public void listar()
    {
        DefaultListModel modelo = new DefaultListModel();
        this.jListFuncionario.setModel(modelo); 
        
        for(Funcionario f : DadosCoordenador.getDados())
        {
            modelo.addElement("Coordenador:"+f.getNome());
        }
        for(Funcionario f : DadosProfessor.getDados())
        {
            modelo.addElement("Professor:"+f.getNome());
        }
        for(Funcionario f : DadosTecnico.getDados())
        {
            modelo.addElement("Tecnico:"+f.getNome());
        }
        
    }

