    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
         FolhaPagamento fp = new FolhaPagamento();
         Funcionario f=null;
         
         int cCoordenador=DadosCoordenador.getDados().size();
         int cProfessor=DadosProfessor.getDados().size();
         int cTecnico=DadosTecnico.getDados().size();
         
         
         int index  = this.jListFuncionario.getSelectedIndex();
         
         if(index<cCoordenador)f=DadosCoordenador.getDados().get(index);
         if(index<cProfessor+cCoordenador&&index>=cCoordenador)f=DadosProfessor.getDados().get(index-cCoordenador);
         if(index<cTecnico+cCoordenador+cProfessor&&index>=cProfessor+cCoordenador)f=DadosProfessor.getDados().get(index-cCoordenador-cProfessor);
         
         
         
         this.irrf.setText(Double.toString(fp.calcIRRF(f)));
         this.inss.setText(Double.toString(fp.calcINSS(f)));
         this.sLiquido.setText(Double.toString(f.calcSalario()-(fp.calcIRRF(f)+fp.calcINSS(f)) ) );
         this.sBruto.setText(Double.toString(f.calcSalario()));
         this.nome.setText(f.getNome());
         
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

