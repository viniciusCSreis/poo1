    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        // TODO add your handling code here:

        int erros=0;

        tirrarColorError();

        String codigo = this.codigo.getText();
        String titulo = this.titulo.getText();
        String data_de_inico=this.dtInicio.getText();
        String data_de_fim=this.dtFim.getText();
        Professor p=null;

        //getProfessor
        int index = this.jListProf.getSelectedIndex();
        if(index>-1){p=DadosProfessor.getDados().get(index);}
        else{erros++;this.jPanelProf.setBackground(main.colorError);}

        //sets
        ProjetoPesquisa pp = new ProjetoPesquisa();
        if(!pp.setCodigo(codigo)){erros++;this.codigo.setBackground(main.colorError);}
        if(!pp.setTitulo(titulo)){erros++;this.titulo.setBackground(main.colorError);}
        if(!pp.setDtFim(data_de_fim)){erros++;this.dtFim.setBackground(main.colorError);}
        if(!pp.setDtInicio(data_de_inico)){erros++;this.dtInicio.setBackground(main.colorError);}
            
        //equipe
        int equipe=0;
        int selected[]=this.jListEquipeProf.getSelectedIndices();
        equipe+=selected.length;
        for(int i=0;i<selected.length;i++)
        {
            pp.adicionar(DadosProfessor.getDados().get(selected[i]));
        }

        selected=this.jListEquipeAlunoGraduacao.getSelectedIndices();
        equipe+=selected.length;
        for(int i=0;i<selected.length;i++)
        {
            pp.adicionar(DadosAlunoGraduacao.getDados().get(selected[i]));
        }
        selected=this.jListEquipeAlunoPosGraduacao.getSelectedIndices();
        equipe+=selected.length;
        for(int i=0;i<selected.length;i++)
        {
            pp.adicionar(DadosAlunoPosGraduacao.getDados().get(selected[i]));
        }
        if(equipe==0)
        {
            erros++;
            this.jPanelEquipe.setBackground(main.colorError);
        }

        if(erros==0)
        {
            pp.setProfessorResponsavel(p);
            DadosProjetoPesquisa.cadastrar(pp);
            JOptionPane.showMessageDialog(null, "Projeto Pesquisa Cadastrado");
        }

    }//GEN-LAST:event_CadastrarActionPerformed

