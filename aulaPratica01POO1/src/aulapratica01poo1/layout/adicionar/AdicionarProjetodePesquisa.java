      private void listar()
      {
          DefaultListModel modelo = new DefaultListModel();
          
          jListProf.setModel(modelo);
          
          for(Professor p : DadosProfessor.getDados())
          {
              modelo.addElement(p.getNome());
          }
          //-----//
          modelo = new DefaultListModel();
          jListEquipeProf.setModel(modelo);
          
          for(Professor p : DadosProfessor.getDados())
          {
              modelo.addElement(p.getNome());
          }
          //-----//
          modelo = new DefaultListModel();
          jListEquipeAlunoGraduacao.setModel(modelo);
          
          for(AlunoGraduacao p : DadosAlunoGraduacao.getDados())
          {
              modelo.addElement(p.getNome());
          }
          //-----//
          modelo = new DefaultListModel();
          jListEquipeAlunoPosGraduacao.setModel(modelo);
          
          for(AlunoPosGraduacao p : DadosAlunoPosGraduacao.getDados())
          {
              modelo.addElement(p.getNome());
          }
      }
